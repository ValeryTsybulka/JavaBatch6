package com.syntax.class23;

public class Sub {
	int sub(int num1, int num2, int num3, int num4){
	    int a=num1-num2-num3-num4;
	    return a;
	  }
	  int sub(int num1, int num2, int num3){
	    int a=num1-num2-num3;
	    return a;
	  }
	  int sub(int num1, int num2){
	    int a=num1-num2;
	    return a;
	  }
}
